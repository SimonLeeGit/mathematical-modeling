%Guss-Seidel迭代法
x=[];
%请输入初始向量X0于X(:,1)中
x(:,1)=[4 -2 6 1]';
%请输入方程组系数矩阵A
A=[16 4 8 4
    4 10 8 4
    8 8 12 10
    4 4 10 12];
%请输入常数向量b
b=[32 26 38 30]';
L=-tril(A,-1)
U=-triu(A,1)
D=A+L+U
x(:,2)=inv(D-L)*U*x(:,1)+inv(D-L)*b;
n=2;
while(x(:,n)-x(:,n-1)>0.00001|x(:,n-1)-x(:,n)>0.00001)
    x(:,n+1)=inv(D-L)*U*x(:,n)+inv(D-L)*b;
    n=n+1;
end
x=x(:,n)